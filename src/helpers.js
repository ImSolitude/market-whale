export function formatCurrency(dollars) {
  // isNaN(cents) ? (cents = 0) : '';
  if(isNaN(dollars)){
    dollars = 0;
  }
  return (dollars).toLocaleString('en-US', {
    style: 'currency',
    currency: 'USD'
  });
}

export function randomArrayValue(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

